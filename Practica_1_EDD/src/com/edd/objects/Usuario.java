package com.edd.objects;

/**
 *
 * @author Shuten-Satsu
 */
public class Usuario {
    private String nombre;
    private int cantidadObjects;

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the cantidadObjects
     */
    public int getCantidadObjects() {
        return cantidadObjects;
    }

    /**
     * @param cantidadObjects the cantidadObjects to set
     */
    public void setCantidadObjects(int cantidadObjects) {
        this.cantidadObjects = cantidadObjects;
    }
    
    
}
